#include <Bitmap.h>

class bits_format {
	public:
		BRect		bounds;
		color_space	cspace;
		int32		extra_count;
		char		extra_bytes[4];
	};

BBitmap * getVSTlogo ()
{
	// private coding of the bitmap. Not that efficient (for compression),
	// but very lightweight! Much faster & memory savy than translators
	// Kind of rle compression.
	bits_format*	format = (bits_format*) vstlogoblack;
	BBitmap* bm = new BBitmap (format->bounds, format->cspace);
	if (format->extra_count < 0)
		memcpy (bm->Bits (), &vstlogoblack[sizeof (bits_format)], -format->extra_count);
	else
	{
		// with rle
		int32*	input = (int32*) &vstlogoblack[sizeof (bits_format)];
		int32*	output = (int32*) bm->Bits ();
		int32	n;
		while ((n = *input) != 0)
		{
			if (n < 0)
			{
				int32 c = -n;
				int32 v = input[1];
				while (--c >= 0)
					output[c] = v;
				output += (-n);
				input += 2;
			}
			else
			{
				memcpy (output, &input[1], n * 4);
				output += n;
				input += n + 1;
			}
		}
		if (format->extra_count > 0)
		{
			char* out = (char*) output;
			int c = format->extra_count;
			while (--c >= 0)
				out[c] = format->extra_bytes[c];
		}
	}
	return bm;
}
